#include "ft_printf.h"

void				putnbr(long n)
{
	char	tmp;

	if (n < 0)
	{
		n = n * -1;
	}
	if (n >= 10)
		putnbr(n / 10);
	tmp = '0' + n % 10;
	write(1, &tmp, 1);
}

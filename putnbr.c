#include "ft_printf.h"

void				putnbr(int n)
{
	long	nb;
	char	tmp;

	nb = (long)n;
	if (nb < 0)
	{
		nb = nb * -1;
	}
	if (nb >= 10)
		putnbr(nb / 10);
	tmp = '0' + n % 10;
	write(1, &tmp, 1);
}
